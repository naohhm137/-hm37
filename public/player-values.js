/**
 * Format value for display
 * @param {number} val - Value in €M
 * @returns {string} Formatted string like "€50M" or "€1.5M"
 */
function formatValue(val) {
  if (!val) return '';
  if (val >= 100) return `€${val}M`;
  if (val >= 10) return `€${val}M`;
  if (val >= 1) return `€${val}M`;
  return `€${val}M`;
}
